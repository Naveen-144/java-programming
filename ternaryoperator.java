// Syntax
// variable = (condition)?valueiftrue:valueiffalse
public class ternaryoperator {
    @SuppressWarnings("unused")
    public static void main(String[] args) {
        String a = 5>2?"yes":"no";
        System.out.println(a);
        int b = 5<2?0:1;
        System.out.println(b);


    }

    
}
