public class FormatExample {
    
        public static void main(String[] args) {
            int age = 25;
            double pi = 3.14159;
            String name = "Alice";
    
            System.out.printf("Name: %s%n", name);          // String
            System.out.printf("Age: %d years%n", age);      // Integer
            System.out.printf("Pi: %.2f%n", pi);            // Float with 2 decimals
        }
    }
    
    

